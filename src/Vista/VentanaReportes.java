    public void llenarTablas(){
     ///////////// LIMPIAR LA TABLA DE CLIENTES   
     while(modelo.getRowCount()>0)
            modelo.removeRow(0);
    /////////////// LIMPIAR LA TABLA DE TRABAJOS
     while(modeloTablaTrabajos.getRowCount()>0)
         modeloTablaTrabajos.removeRow(0);
    
     //////////// LLENANDO LA TABLA DE CLIENTES //////////////////
     for(int i=0;i<op.getClientes().size();i++){
        String nombre= op.getClientes().get(i).getNombre();
        String id= op.getClientes().get(i).getIdentificacion();
        String direccion= op.getClientes().get(i).getDireccion();
        String telefono= op.getClientes().get(i).getTelefono();
        String correo = op.getClientes().get(i).getCorreo();
        String cargo = op.getClientes().get(i).getCargo();
        String matricula="";
        
        for(int j=0; j<op.getClientes().get(i).getVehiculo().size();j++){
            matricula = op.getClientes().get(i).getVehiculo().get(j).getMatricula();
           }
        
    String datos2[]={nombre,id,telefono,direccion,correo,cargo,matricula};
    modelo.addRow(datos2);
    }
     
    ////////////// LLENANDO LA TABLA DE TRABAJOS ///////////////////
     String matric = "";
     String piezas = "";
    for(int i = 0; i<op.getClientes().size(); i++){
         
        for(int j=0; j<op.getClientes().get(i).getMecanico().size(); j++){
            
            for(int k = 0; k<op.getClientes().get(i).getMecanico().get(j).getTrabajos().size(); k++){
                
                String codigo = op.getClientes().get(i).getMecanico().get(j).getTrabajos().get(k).getCodigo();
                int hora = op.getClientes().get(i).getMecanico().get(j).getTrabajos().get(k).getNumeroHora();
                int plazo = op.getClientes().get(i).getMecanico().get(j).getTrabajos().get(k).getPlazoMaximo();
                String tipo = op.getClientes().get(i).getMecanico().get(j).getTrabajos().get(k).getTipoTrabajo();
                double valor = op.getClientes().get(i).getMecanico().get(j).getValorPagar();
                    
               
                for(int h=0;h<op.getClientes().get(i).getMecanico().get(j).getTrabajos().get(k).getPiezas().size(); h++ ){
                   
                    piezas =""+op.getClientes().get(i).getMecanico().get(j).getTrabajos().get(k).getPrecioMaterial();
                }
               
                for(int l=0; l<op.getClientes().get(i).getVehiculo().size();l++){
                         matric = op.getClientes().get(i).getVehiculo().get(l).getMatricula();
                        }
                String fecha = op.getClientes().get(i).getMecanico().get(j).getFechaIngreso();
                String datos3[]={codigo,matric,fecha,""+hora,""+plazo/24+" dias",tipo,piezas,""+valor};
                modeloTablaTrabajos.addRow(datos3);
            }   
        }
    
    }
  
    }
    
    
   
     
     
